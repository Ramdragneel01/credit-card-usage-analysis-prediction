@app.get("/health/readiness")
def readiness() -> dict[str, object]:
    """Return readiness state with optional model dependency enforcement."""

    trained = _is_model_trained()
    require_model = _require_model_for_readiness()
    payload: dict[str, object] = {
        "status": "ready",
        "trained": trained,
        "require_model_for_readiness": require_model,
    }

    if require_model and not trained:
        payload["status"] = "not_ready"
        payload["reason"] = "model_not_trained"
        return JSONResponse(status_code=503, content=payload)

    return payload


@app.post("/train/demo")
def train_demo(
    samples: Annotated[int, Query(ge=100, le=20000)] = 1500,
    segment_count: Annotated[int, Query(ge=2, le=20)] = 4,
    _: AuthDep = None,
    __: RateLimitDep = None,
) -> dict[str, object]:
    dataset = generate_demo_dataset(samples=samples)
    artifacts = train_models(dataset, segment_count=segment_count)
    with state_lock:
        state["artifacts"] = artifacts

    segment_distribution = summarize_segments(
        artifacts,
        dataset[FEATURE_COLUMNS].to_dict(orient="records"),
    )

    LOGGER.info("Training completed rows=%s segment_count=%s", len(dataset), segment_count)

    return {
        "status": "trained",
        "rows": len(dataset),
        "segment_count": segment_count,
        "baseline_accuracy": round(artifacts.baseline_accuracy, 4),
        "segment_distribution": segment_distribution,
    }


