@app.middleware("http")
async def add_runtime_security_headers(request: Request, call_next):
    """Apply request limits and baseline security headers to all responses."""

    request_id = request.headers.get("X-Request-ID") or str(uuid.uuid4())
    request_start = time.monotonic()
    max_request_bytes = _get_max_request_bytes()

    content_length = request.headers.get("content-length")
    if content_length is not None:
        try:
            content_length_value = int(content_length)
        except ValueError:
            return JSONResponse(status_code=400, content={"detail": "Invalid Content-Length header."})

        if content_length_value > max_request_bytes:
            return JSONResponse(status_code=413, content={"detail": "Request body too large."})

    if request.method in {"POST", "PUT", "PATCH"}:
        body = await request.body()
        if len(body) > max_request_bytes:
            return JSONResponse(status_code=413, content={"detail": "Request body too large."})

    response = await call_next(request)
    duration_ms = (time.monotonic() - request_start) * 1000

    response.headers["X-Request-ID"] = request_id
    response.headers["X-Process-Time-Ms"] = f"{duration_ms:.2f}"
    response.headers["X-Content-Type-Options"] = "nosniff"
    response.headers["X-Frame-Options"] = "DENY"
    response.headers["Referrer-Policy"] = "no-referrer"
    response.headers["Permissions-Policy"] = "camera=(), geolocation=(), microphone=()"
    response.headers["Cache-Control"] = "no-store"
    if request.url.scheme == "https":
        response.headers["Strict-Transport-Security"] = "max-age=63072000; includeSubDomains"

    return response


