from __future__ import annotations

import os
import time
from collections import defaultdict, deque
from typing import Annotated, Any

from fastapi import Depends, FastAPI, Header, HTTPException, Query, Request
from pydantic import BaseModel, Field

from src.analysis import (
    FEATURE_COLUMNS,
    TrainingArtifacts,
    generate_demo_dataset,
    predict_activity,
    score_anomalies,
    summarize_segments,
    train_models,
)


class BatchRequest(BaseModel):
    records: list[dict[str, Any]] = Field(min_length=1, max_length=2000)


app = FastAPI(
    title="credit-card-usage-analysis-prediction",
    version="0.1.0",
)

state: dict[str, TrainingArtifacts | None] = {"artifacts": None}
rate_limit_buckets: dict[str, deque[float]] = defaultdict(deque)


# Generated by GitHub Copilot
def _get_configured_api_key() -> str:
    """Return configured API key, or empty string when auth is disabled."""

    return os.getenv("API_KEY", "").strip()


def _get_rate_limit_max() -> int:
    """Return the max requests allowed in a rate-limit window."""

    try:
        limit = int(os.getenv("RATE_LIMIT_MAX", "60"))
    except ValueError:
        return 60
    return max(1, limit)


def _get_rate_limit_window_seconds() -> int:
    """Return rate-limit window size in seconds."""

    try:
        window = int(os.getenv("RATE_LIMIT_WINDOW_SECONDS", "60"))
    except ValueError:
        return 60
    return max(1, window)


def clear_rate_limit_state() -> None:
    """Reset in-memory rate-limit counters for deterministic tests."""

    rate_limit_buckets.clear()


def _require_api_key(
    x_api_key: Annotated[str | None, Header(alias="X-API-Key")] = None,
) -> None:
    """Validate X-API-Key header when API_KEY is configured."""

    configured_api_key = _get_configured_api_key()
    if not configured_api_key:
        return

    if x_api_key != configured_api_key:
        raise HTTPException(status_code=401, detail="Unauthorized")


def _enforce_rate_limit(request: Request) -> None:
    """Apply in-memory rate limiting per client and endpoint path."""

    limit = _get_rate_limit_max()
    window_seconds = _get_rate_limit_window_seconds()

    client_host = request.client.host if request.client else "unknown"
    bucket_key = f"{client_host}:{request.url.path}"

    now = time.monotonic()
    cutoff = now - window_seconds
    bucket = rate_limit_buckets[bucket_key]

    while bucket and bucket[0] < cutoff:
        bucket.popleft()

    if len(bucket) >= limit:
        raise HTTPException(status_code=429, detail="Rate limit exceeded")

    bucket.append(now)


AuthDep = Annotated[None, Depends(_require_api_key)]
RateLimitDep = Annotated[None, Depends(_enforce_rate_limit)]


# Generated by GitHub Copilot
def _ensure_artifacts() -> TrainingArtifacts:
    """Return trained artifacts or raise a clear 400 error when unavailable."""

    artifacts = state.get("artifacts")
    if artifacts is None:
        raise HTTPException(status_code=400, detail="Model artifacts not trained yet. Call POST /train/demo first.")
    return artifacts


@app.get("/health")
def health() -> dict[str, object]:
    return {
        "status": "ok",
        "trained": state.get("artifacts") is not None,
        "feature_columns": FEATURE_COLUMNS,
    }


@app.post("/train/demo")
def train_demo(
    samples: Annotated[int, Query(ge=100, le=20000)] = 1500,
    segment_count: Annotated[int, Query(ge=2, le=20)] = 4,
    _: AuthDep = None,
    __: RateLimitDep = None,
) -> dict[str, object]:
    dataset = generate_demo_dataset(samples=samples)
    artifacts = train_models(dataset, segment_count=segment_count)
    state["artifacts"] = artifacts

    segment_distribution = summarize_segments(
        artifacts,
        dataset[FEATURE_COLUMNS].to_dict(orient="records"),
    )

    return {
        "status": "trained",
        "rows": len(dataset),
        "segment_count": segment_count,
        "baseline_accuracy": round(artifacts.baseline_accuracy, 4),
        "segment_distribution": segment_distribution,
    }


@app.get("/segments/summary")
def segments_summary(
    limit: Annotated[int, Query(ge=10, le=5000)] = 600,
    _: AuthDep = None,
    __: RateLimitDep = None,
) -> dict[str, object]:
    artifacts = _ensure_artifacts()
    dataset = generate_demo_dataset(samples=limit)
    summary = summarize_segments(
        artifacts,
        dataset[FEATURE_COLUMNS].to_dict(orient="records"),
    )
    return {
        "rows": len(dataset),
        "summary": summary,
    }


@app.post("/predict/activity")
def predict_activity_endpoint(
    payload: BatchRequest,
    _: AuthDep = None,
    __: RateLimitDep = None,
) -> dict[str, object]:
    artifacts = _ensure_artifacts()

    try:
        predictions = predict_activity(artifacts, payload.records)
    except ValueError as exc:
        raise HTTPException(status_code=422, detail=str(exc)) from exc

    return {
        "count": len(predictions),
        "predictions": predictions,
    }


@app.post("/predict/anomaly")
def predict_anomaly_endpoint(
    payload: BatchRequest,
    _: AuthDep = None,
    __: RateLimitDep = None,
) -> dict[str, object]:
    artifacts = _ensure_artifacts()

    try:
        scores = score_anomalies(artifacts, payload.records)
    except ValueError as exc:
        raise HTTPException(status_code=422, detail=str(exc)) from exc

    return {
        "count": len(scores),
        "scores": scores,
    }
