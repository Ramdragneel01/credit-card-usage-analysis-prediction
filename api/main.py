# Generated by GitHub Copilot
def _require_api_key(
    x_api_key: Annotated[str | None, Header(alias="X-API-Key")] = None,
) -> None:
    """Validate X-API-Key header when API_KEY is configured."""

    configured_api_key = _get_configured_api_key()
    if not configured_api_key:
        return

    if x_api_key is None or not secrets.compare_digest(x_api_key, configured_api_key):
        raise HTTPException(status_code=401, detail="Unauthorized")


# Generated by GitHub Copilot
def _enforce_rate_limit(request: Request) -> None:
    """Apply in-memory rate limiting per client and endpoint path."""

    limit = _get_rate_limit_max()
    window_seconds = _get_rate_limit_window_seconds()

    client_host = request.client.host if request.client else "unknown"
    bucket_key = f"{client_host}:{request.url.path}"

    now = time.monotonic()
    cutoff = now - window_seconds

    with rate_limit_lock:
        bucket = rate_limit_buckets[bucket_key]

        while bucket and bucket[0] < cutoff:
            bucket.popleft()

        if len(bucket) >= limit:
            raise HTTPException(status_code=429, detail="Rate limit exceeded")

        bucket.append(now)


