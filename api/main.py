@app.post("/predict/activity")
def predict_activity_endpoint(
    payload: BatchRequest,
    _: AuthDep = None,
    __: RateLimitDep = None,
) -> dict[str, object]:
    artifacts = _ensure_artifacts()
    records = _records_to_dicts(payload.records)

    try:
        predictions = predict_activity(artifacts, records)
    except ValueError as exc:
        raise HTTPException(status_code=422, detail=str(exc)) from exc

    return {
        "count": len(predictions),
        "predictions": predictions,
    }


