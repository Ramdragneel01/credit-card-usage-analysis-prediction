from __future__ import annotations

from typing import Any

from fastapi import FastAPI, HTTPException
from pydantic import BaseModel, Field

from src.analysis import (
    FEATURE_COLUMNS,
    TrainingArtifacts,
    generate_demo_dataset,
    predict_activity,
    score_anomalies,
    summarize_segments,
    train_models,
)


class BatchRequest(BaseModel):
    records: list[dict[str, Any]] = Field(min_length=1, max_length=2000)


app = FastAPI(
    title="credit-card-usage-analysis-prediction",
    version="0.1.0",
)

state: dict[str, TrainingArtifacts | None] = {"artifacts": None}


# Generated by GitHub Copilot
def _ensure_artifacts() -> TrainingArtifacts:
    """Return trained artifacts or raise a clear 400 error when unavailable."""

    artifacts = state.get("artifacts")
    if artifacts is None:
        raise HTTPException(status_code=400, detail="Model artifacts not trained yet. Call POST /train/demo first.")
    return artifacts


@app.get("/health")
def health() -> dict[str, object]:
    return {
        "status": "ok",
        "trained": state.get("artifacts") is not None,
        "feature_columns": FEATURE_COLUMNS,
    }


@app.post("/train/demo")
def train_demo(samples: int = 1500, segment_count: int = 4) -> dict[str, object]:
    dataset = generate_demo_dataset(samples=samples)
    artifacts = train_models(dataset, segment_count=segment_count)
    state["artifacts"] = artifacts

    segment_distribution = summarize_segments(
        artifacts,
        dataset[FEATURE_COLUMNS].to_dict(orient="records"),
    )

    return {
        "status": "trained",
        "rows": len(dataset),
        "segment_count": segment_count,
        "baseline_accuracy": round(artifacts.baseline_accuracy, 4),
        "segment_distribution": segment_distribution,
    }


@app.get("/segments/summary")
def segments_summary(limit: int = 600) -> dict[str, object]:
    artifacts = _ensure_artifacts()
    dataset = generate_demo_dataset(samples=limit)
    summary = summarize_segments(
        artifacts,
        dataset[FEATURE_COLUMNS].to_dict(orient="records"),
    )
    return {
        "rows": len(dataset),
        "summary": summary,
    }


@app.post("/predict/activity")
def predict_activity_endpoint(payload: BatchRequest) -> dict[str, object]:
    artifacts = _ensure_artifacts()

    try:
        predictions = predict_activity(artifacts, payload.records)
    except ValueError as exc:
        raise HTTPException(status_code=422, detail=str(exc)) from exc

    return {
        "count": len(predictions),
        "predictions": predictions,
    }


@app.post("/predict/anomaly")
def predict_anomaly_endpoint(payload: BatchRequest) -> dict[str, object]:
    artifacts = _ensure_artifacts()

    try:
        scores = score_anomalies(artifacts, payload.records)
    except ValueError as exc:
        raise HTTPException(status_code=422, detail=str(exc)) from exc

    return {
        "count": len(scores),
        "scores": scores,
    }
