from __future__ import annotations

from dataclasses import dataclass
from typing import Any

import numpy as np
import pandas as pd
from sklearn.cluster import KMeans
from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import accuracy_score
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler


FEATURE_COLUMNS = [
    "avg_ticket",
    "monthly_txn_count",
    "online_ratio",
    "weekend_ratio",
    "category_entropy",
    "chargeback_ratio",
]


@dataclass
class TrainingArtifacts:
    scaler: StandardScaler
    segment_model: KMeans
    activity_model: RandomForestClassifier
    baseline_accuracy: float


# Generated by GitHub Copilot
def generate_demo_dataset(samples: int = 1500, seed: int = 42) -> pd.DataFrame:
    """Create deterministic synthetic card behavior records for local model tests."""

    rng = np.random.default_rng(seed)

    avg_ticket = rng.normal(63, 19, samples).clip(5, 240)
    monthly_txn_count = rng.normal(28, 11, samples).clip(2, 120)
    online_ratio = rng.uniform(0.05, 0.95, samples)
    weekend_ratio = rng.uniform(0.05, 0.8, samples)
    category_entropy = rng.normal(1.8, 0.5, samples).clip(0.2, 3.2)
    chargeback_ratio = rng.beta(1.2, 18, samples).clip(0.0, 0.45)

    activity_score = (
        0.25 * (monthly_txn_count / monthly_txn_count.max())
        + 0.2 * online_ratio
        + 0.2 * weekend_ratio
        + 0.15 * (category_entropy / category_entropy.max())
        + 0.15 * (avg_ticket / avg_ticket.max())
        - 0.35 * chargeback_ratio
    )

    is_active = (activity_score > np.quantile(activity_score, 0.47)).astype(int)

    df = pd.DataFrame(
        {
            "avg_ticket": avg_ticket,
            "monthly_txn_count": monthly_txn_count,
            "online_ratio": online_ratio,
            "weekend_ratio": weekend_ratio,
            "category_entropy": category_entropy,
            "chargeback_ratio": chargeback_ratio,
            "is_active": is_active,
        }
    )
    return df


# Generated by GitHub Copilot
def _to_frame(records: list[dict[str, Any]]) -> pd.DataFrame:
    """Normalize inbound records into a DataFrame with required feature columns."""

    frame = pd.DataFrame(records)
    missing = [column for column in FEATURE_COLUMNS if column not in frame.columns]
    if missing:
        raise ValueError(f"Missing required feature columns: {', '.join(missing)}")
    return frame[FEATURE_COLUMNS].astype(float)


# Generated by GitHub Copilot
def train_models(df: pd.DataFrame, segment_count: int = 4, seed: int = 42) -> TrainingArtifacts:
    """Train segmentation and activity models from prepared behavior records."""

    features = df[FEATURE_COLUMNS].astype(float)
    labels = df["is_active"].astype(int)

    scaler = StandardScaler()
    scaled = scaler.fit_transform(features)

    segment_model = KMeans(n_clusters=segment_count, random_state=seed, n_init=20)
    segment_model.fit(scaled)

    x_train, x_test, y_train, y_test = train_test_split(
        scaled,
        labels,
        test_size=0.25,
        random_state=seed,
        stratify=labels,
    )

    activity_model = RandomForestClassifier(
        n_estimators=180,
        max_depth=8,
        random_state=seed,
    )
    activity_model.fit(x_train, y_train)

    predictions = activity_model.predict(x_test)
    baseline_accuracy = float(accuracy_score(y_test, predictions))

    return TrainingArtifacts(
        scaler=scaler,
        segment_model=segment_model,
        activity_model=activity_model,
        baseline_accuracy=baseline_accuracy,
    )


# Generated by GitHub Copilot
def predict_activity(artifacts: TrainingArtifacts, records: list[dict[str, Any]]) -> list[dict[str, float | int]]:
    """Predict activity class and confidence for a batch of customer records."""

    frame = _to_frame(records)
    scaled = artifacts.scaler.transform(frame)

    labels = artifacts.activity_model.predict(scaled)
    confidence = artifacts.activity_model.predict_proba(scaled)[:, 1]

    output: list[dict[str, float | int]] = []
    for index, label in enumerate(labels):
        output.append(
            {
                "is_active": int(label),
                "confidence": float(round(confidence[index], 4)),
            }
        )
    return output


