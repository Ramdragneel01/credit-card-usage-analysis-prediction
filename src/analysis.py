def score_anomalies(artifacts: TrainingArtifacts, records: list[dict[str, Any]]) -> list[float]:
    """Score anomalies as normalized distance from closest customer segment centroid."""

    frame = _to_frame(records)
    scaled = artifacts.scaler.transform(frame)

    centroids = artifacts.segment_model.cluster_centers_
    distances = []
    for row in scaled:
        point_distances = np.linalg.norm(centroids - row, axis=1)
        distances.append(float(np.min(point_distances)))

    max_distance = max(distances) if distances else 1.0
    if max_distance <= 0:
        return [0.0 for _ in distances]

    return [round(min(1.0, distance / max_distance), 4) for distance in distances]


# Generated by GitHub Copilot
def summarize_segments(artifacts: TrainingArtifacts, records: list[dict[str, Any]]) -> dict[str, int]:
    """Return cluster distribution map for dashboard-style segment views."""

    frame = _to_frame(records)
    scaled = artifacts.scaler.transform(frame)
    segments = artifacts.segment_model.predict(scaled)

    summary: dict[str, int] = {}
    for segment in segments:
        key = f"segment_{int(segment)}"
        summary[key] = summary.get(key, 0) + 1
    return summary
